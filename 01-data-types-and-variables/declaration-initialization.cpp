#include <iostream>
using namespace std;

int main() {
    // Variable declaration and initialization
    int myInteger = 42;
    double myDouble = 3.14;
    char myChar = 'A';

    // Output the values of the variables
    cout << "Integer: " << myInteger << endl;
    cout << "Double: " << myDouble << endl;
    cout << "Character: " << myChar << endl;

    return 0;
}