#include <iostream>
using namespace std;

int main() {
    int a = 10;
    int b = 5;
    int result;

    // Addition
    result = a + b;
    cout << "Addition: " << result << endl;

    // Subtraction
    result = a - b;
    cout << "Subtraction: " << result << endl;

    // Multiplication
    result = a * b; 
    cout << "Multiplication: " << result << endl;

    // Division
    result = a / b;
    cout << "Division: " << result << endl;

    return 0;
}