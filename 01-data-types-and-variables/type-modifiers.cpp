#include <iostream>
using namespace std;

int main() {
    // Signed modifier
    signed int signedNumber = -10;
    cout << "Signed int: " << signedNumber << endl;

    // Unsigned modifier
    unsigned int unsignedNumber = 20;
    cout << "Unsigned int: " << unsignedNumber << endl;

    // Long modifier
    long longNumber = 1000000L;
    cout << "Long: " << longNumber << endl;

    // Short modifier
    short shortNumber = -30000;
    cout << "Short: " << shortNumber << endl;

    return 0;
}